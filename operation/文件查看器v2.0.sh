#!/bin/bash
#文件查看器v2.0
num=3
until [ $num == 0 ]
	do
echo "***********************************************************************"	
	ls | cat -n 
echo "***********************************************************************"
read -t 30 -p "请输入你要查看的文件的行号，退出请按0: " num
if [ ! $num == 0 ];then
tmp=$(ls | sed -n "$num"p)
clear
cat $tmp
fi
	done	
